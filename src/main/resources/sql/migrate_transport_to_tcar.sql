-- 5. TCAR_TRANSPORT_FILE 테이블 생성 (장표 첨부파일)
CREATE TABLE TCAR_TRANSPORT_FILE (
    FILE_ID         NUMBER          PRIMARY KEY,
    TRANSPORT_ID    NUMBER          NOT NULL,
    ORIGINAL_NAME   VARCHAR2(500)   NOT NULL,
    SAVED_NAME      VARCHAR2(500)   NOT NULL,
    FILE_PATH       VARCHAR2(1000),
    FILE_SIZE       NUMBER(15)      DEFAULT 0,
    FILE_EXT        VARCHAR2(20),
    CREATED_AT      DATE            DEFAULT SYSDATE,
    CONSTRAINT FK_TCAR_TRANSPORT_FILE
        FOREIGN KEY (TRANSPORT_ID) REFERENCES TCAR_TRANSPORT(ID)
);

-- 6. TCAR_TRANSPORT_FILE 시퀀스 생성
CREATE SEQUENCE TCAR_TRANSPORT_FILE_SEQ START WITH 1 INCREMENT BY 1 NOCACHE;

