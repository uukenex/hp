-- 1. TCAR_TRANSPORT 테이블 생성
CREATE TABLE TCAR_TRANSPORT (
    ID               NUMBER          PRIMARY KEY,
    TRANSPORT_DATE   DATE            NOT NULL,
    DRIVER_NAME      VARCHAR2(100),
    COMPANY          VARCHAR2(100),
    LOADING_POINT    VARCHAR2(200),
    UNLOADING_POINT  VARCHAR2(200),
    CAR_MODEL        VARCHAR2(100),
    VEHICLE_NO       VARCHAR2(50),
    SUPPLY_PRICE     NUMBER(15)      DEFAULT 0,
    COMPANY_PRICE    NUMBER(15)      DEFAULT 0,
    CREATED_AT       DATE            DEFAULT SYSDATE
);

-- 2. TCAR_TRANSPORT 시퀀스 생성
CREATE SEQUENCE TCAR_TRANSPORT_SEQ START WITH 1 INCREMENT BY 1 NOCACHE;

